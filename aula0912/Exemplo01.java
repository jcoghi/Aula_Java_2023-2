/*
 * Crie um contador regressivo de 10 até 0.
*/

package aula0912;
/**
 * @author prof. João Felipe Coghi
 */
public class Exemplo01 {

    public static void main(String[] args) {
        
       
        for (int a = 10 ; a >= 0; a--) {
            System.out.println("Valor dentro do for " + a);
        }
        
    }

}
