    private void btnEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEntrarActionPerformed
        String username = "joao";
        String password = "123";

        String login = txtLogin.getText();
        String senha = String.valueOf(pwdSenha.getPassword());

        if (login.equalsIgnoreCase(username)) {

            if (senha.equals(password)) {
                JOptionPane.showMessageDialog(rootPane, "Acesso liberado");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Senha Incorreta!");
            }

        } else {
            JOptionPane.showMessageDialog(rootPane, "Digitou o login errado!");
        }
        txtLogin.setEnabled(false);
        pwdSenha.setEditable(false);
        btnEntrar.setEnabled(false);
    }//GEN-LAST:event_btnEntrarActionPerformed

    private void pwdSenhaKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_pwdSenhaKeyPressed
        String username = "joao";
        String password = "123";

        String login = txtLogin.getText();
        String senha = String.valueOf(pwdSenha.getPassword());

        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {

            if (login.equalsIgnoreCase(username)) {

                if (senha.equals(password)) {
                    JOptionPane.showMessageDialog(rootPane, "Acesso liberado");
                } else {
                    JOptionPane.showMessageDialog(rootPane, "Senha Incorreta!");
                }

            } else {
                JOptionPane.showMessageDialog(rootPane, "Digitou o login errado!");
            }
            txtLogin.setEnabled(false);
            pwdSenha.setEditable(false);
            btnEntrar.setEnabled(false);
        }
    }//GEN-LAST:event_pwdSenhaKeyPressed

